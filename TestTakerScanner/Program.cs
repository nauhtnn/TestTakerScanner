    {
        const bool useMethod2 = true;
        // Replace <Subscription Key> with your valid subscription key.
        const string subscriptionKey = "26b4d99217f645818a4d2e049f3865fc";

        // You must use the same Azure region in your REST API method as you used to
        // get your subscription keys. For example, if you got your subscription keys
        // from the West US region, replace "westcentralus" in the URL
        // below with "westus".
        //
        // Free trial subscription keys are generated in the "westus" region.
        // If you use a free trial subscription key, you shouldn't need to change
        // this region.
        const string uriBase =
            "https://southeastasia.api.cognitive.microsoft.com/vision/v2.0/ocr";
        const string uriBase2 =
            "https://southeastasia.api.cognitive.microsoft.com/vision/v2.0/recognizeText";

        static void Main()
        {
            // Get the path and filename to process from the user.
            //Console.WriteLine("Analyze an image:");
            //Console.Write(
